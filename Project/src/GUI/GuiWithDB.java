    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(dlm.isEmpty()){
            JOptionPane.showMessageDialog(this, "Error Occured! Please Try Inserting Some Data First!");
        }
        if(!dlm.isEmpty()){
            String name = jList2.getSelectedValue();
            Connection connection = DataBase.establishConnectioWithDB();
            if(jList1.getSelectedValue() == "Person"){
                try {
                    ArrayList<Person> person = DataBase.selectPersonFromDB(connection, name);
                    for(Person per:person){
                        jTextArea1.append(per.personToString());
                        jTextArea1.append("\n");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error Occured!");
                }
            } 
            if(jList1.getSelectedValue() == "Album"){
                try {
                    ArrayList<Album> album = DataBase.selectAlbumFromDB(connection, name);
                    for(Album al:album){
                        jTextArea1.append(al.albumToString());
                        jTextArea1.append("\n");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error Occured!");
                }
            } 
        }
    }//GEN-LAST:event_jButton3ActionPerformed


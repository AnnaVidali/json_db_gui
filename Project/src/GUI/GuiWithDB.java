    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name;
        try{
            if(jList1.getSelectedValue() != "Person" || jList1.getSelectedValue() != "Album"){
                JOptionPane.showMessageDialog(this, "Error Occured! Please Choose Between Person And Album First!");
            }
            if(jList1.getSelectedValue() == "Person"){
                name = jTextField1.getText();
                Connection connection = DataBase.establishConnectioWithDB();
                ArrayList<Person> p = DataBase.selectPersonFromDB(connection, name);
                int n;
                if(p.isEmpty()){
                    Object[] options = {"Search With API!", "Do Nothing!"};
                    n = JOptionPane.showOptionDialog(this, "Please Choose What To Do Next!", "Choices",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null, options,options[0]);
                    if(n == 0){
                        ArrayList<Person> person = APIWrapper.getPersonByName(name);
                        DataBase.insertPersonArrayToDB(connection, person);
                        for(Person per:person){
                            jTextArea1.append( i + ")   " + per.personToString() + "\n");
                            i++;
                        }
                        jTextArea1.append("\n");
                        i = i-1;
                        JOptionPane.showMessageDialog(this, "The Number of Results is " + i + "!");
                        i = 1;
                        jTextField1.setText("");
                    }
                    if(n == 1){
                        jTextField1.setText("");
                        for(int counter = 0; counter <= i; counter++){
                            jTextArea1.setText("");
                        }
                    }
                }
                if(!p.isEmpty()){
                    ArrayList<Person> person = APIWrapper.getPersonByName(name);
                    for(Person per:person){
                        dlm.addElement(per.getName());
                    }
                }
            }
            if(jList1.getSelectedValue() == "Album"){
                name = jTextField2.getText();
                Connection connection = DataBase.establishConnectioWithDB();
                ArrayList<Album> a = DataBase.selectAlbumFromDB(connection, name);
                int n;
                if(a.isEmpty()){
                    Object[] options = {"Search With API!", "Do Nothing!"};
                    n = JOptionPane.showOptionDialog(this, "Please Choose What To Do Next!", "Choices",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null, options,options[0]);
                    if(n == 0){
                        ArrayList<Album> album = APIWrapper.getAlbumByName(name);
                        DataBase.insertAlbumArrayToDB(connection, album);
                        for(Album al:album){
                            jTextArea1.append( i + ")   " + al.albumToString() + "\n");
                            i++;
                        }
                        jTextArea1.append("\n");
                        i = i-1;
                        JOptionPane.showMessageDialog(this, "The Number of Results is " + i + "!");
                        i = 1;
                        jTextField2.setText("");
                    }
                    if(n == 1){
                        jTextField2.setText("");
                        for(int counter = 0; counter <= i; counter++){
                            jTextArea1.setText("");
                        }
                    }
                }
                if(!a.isEmpty()){
                    ArrayList<Album> album = APIWrapper.getAlbumByName(name);
                    for(Album al:album){
                        dlm.addElement(al.getTitle());
                    }
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error Occured!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

